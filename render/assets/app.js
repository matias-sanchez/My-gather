  // --- 5. Nav-rail collapse + scroll-spy --------------------------

  // Floating nav-drawer controller.
  //
  // The left navigation is an overlay drawer triggered by the logo
  // button pinned to the top-left of the viewport. It always starts
  // CLOSED on page load (no persistence — per product decision) and is
  // opened by clicking the logo, pressing Cmd/Ctrl + `.`, or by
  // following an in-page anchor. It closes when the user clicks the
  // logo again, clicks the backdrop, presses Escape, or activates a
  // link inside the drawer.
  function initNavCollapse() {
    var toggle   = document.getElementById("nav-toggle");
    var drawer   = document.getElementById("nav-drawer");
    var backdrop = document.getElementById("nav-backdrop");
    if (!toggle || !drawer || !backdrop) return;
    var mainEl = document.querySelector("main.content");

    function isOpen() { return document.body.classList.contains("nav-open"); }

    function focusables() {
      return drawer.querySelectorAll(
        'a[href], button:not([disabled]), [tabindex]:not([tabindex="-1"])'
      );
    }

    function setOpen(open, opts) {
      var wasOpen = isOpen();
      // One-modal-at-a-time: opening the drawer unpins and closes any
      // floating mysqladmin panel. Otherwise `mainEl.inert = true` below
      // would strand a user-pinned panel inside an inert subtree (all
      // controls disabled) with no way to reach it.
      if (open) {
        var pinnedPanels = document.querySelectorAll(".ma-panel-host.is-pinned .ma-panel-pin");
        for (var pi = 0; pi < pinnedPanels.length; pi++) {
          pinnedPanels[pi].click();
        }
        var openPanels = document.querySelectorAll(".ma-panel-host.is-open .ma-panel-close");
        for (var ci = 0; ci < openPanels.length; ci++) {
          openPanels[ci].click();
        }
      }
      document.body.classList.toggle("nav-open", !!open);
      toggle.setAttribute("aria-expanded", open ? "true" : "false");
      drawer.setAttribute("aria-hidden", open ? "false" : "true");
      backdrop.setAttribute("aria-hidden", open ? "false" : "true");
      // a11y: make closed drawer non-interactive; when open, make main
      // content inert so Tab stays trapped in the drawer.
      drawer.inert = !open;
      if (mainEl) mainEl.inert = !!open;
      // Prevent background scroll while the drawer is open so the
      // overlay reads as modal.
      document.documentElement.style.overflow = open ? "hidden" : "";
      var manageFocus = !(opts && opts.silent);
      if (manageFocus) {
        if (open) {
          // Move focus into the drawer so keyboard users don't stay
          // stranded on the toggle.
          var first = drawer.querySelector("a, button") || drawer;
          if (first && typeof first.focus === "function") first.focus();
        } else if (wasOpen) {
          // Restore focus to the toggle so the user's keyboard context
          // is preserved.
          if (typeof toggle.focus === "function") toggle.focus();
        }
      }
      // Re-fit charts once the transition settles (content layout
      // doesn't reflow, but browser zoom + window size may have changed
      // while the drawer was open). Under prefers-reduced-motion the
      // transition is near-instant (0.01ms), so don't wait 360ms.
      if (!open) {
        var rmReduce = window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches;
        setTimeout(resizeAllCharts, rmReduce ? 0 : 360);
      }
    }

    toggle.addEventListener("click", function () { setOpen(!isOpen()); });
    backdrop.addEventListener("click", function () { setOpen(false); });

    // Close the drawer when any in-drawer link is clicked — the user
    // has navigated, so the drawer has served its purpose.
    drawer.addEventListener("click", function (e) {
      var t = e.target;
      while (t && t !== drawer) {
        if (t.tagName === "A") { setOpen(false); return; }
        t = t.parentNode;
      }
    });

    // Tab focus trap while the drawer is open. Wraps from last to
    // first (Tab) and first to last (Shift+Tab).
    drawer.addEventListener("keydown", function (e) {
      if (!isOpen() || e.key !== "Tab") return;
      var items = focusables();
      if (items.length === 0) return;
      var first = items[0];
      var last  = items[items.length - 1];
      if (e.shiftKey && document.activeElement === first) {
        e.preventDefault();
        last.focus();
      } else if (!e.shiftKey && document.activeElement === last) {
        e.preventDefault();
        first.focus();
      }
    });

    document.addEventListener("keydown", function (e) {
      if (e.defaultPrevented) return;
      // Escape closes.
      if (e.key === "Escape" && isOpen()) {
        // If the floating mysqladmin panel is open, let its own Esc handler
        // take this keypress — pressing Esc should close the topmost
        // overlay, not a background drawer.
        if (document.querySelector(".ma-panel-host.is-open") && !document.querySelector(".ma-panel-host.is-pinned")) {
          return;
        }
        e.preventDefault();
        setOpen(false);
        return;
      }
      // Cmd/Ctrl + . toggles. (Chose '.' over '\' per product request —
      // easier to reach on most keyboard layouts.)
      if ((e.metaKey || e.ctrlKey) && e.key === ".") {
        e.preventDefault();
        setOpen(!isOpen());
      }
    });

    // Start closed every page-load. Pass silent so we don't steal focus
    // on the initial render.
    setOpen(false, { silent: true });
  }

  function initNavGroups() {
    // The nav groups use a separate localStorage namespace so their
    // collapse state doesn't collide with the main-content section
    // collapse keys (which use ID "sec-os", "sec-db", ...).
    var groups = document.querySelectorAll("nav.index details.nav-group");
    for (var i = 0; i < groups.length; i++) {
      (function (g) {
        var key = "mygather:" + REPORT_ID + ":nav:" + g.id;
        var saved = storageGet(key);
        if (saved === "closed") g.open = false;
        else if (saved === "open") g.open = true;
        g.addEventListener("toggle", function () {
          storageSet(key, g.open ? "open" : "closed");
        });
      })(groups[i]);
    }

    // The summary-title anchor (<a class="nav-title">) is what makes
    // Level-1 groups with no Level-2 children navigable. Clicking it
    // would otherwise also toggle the parent <details>; stopPropagation
    // keeps the semantics clean: anchor = navigate, rest of summary =
    // expand/collapse.
    var titleLinks = document.querySelectorAll("nav.index details.nav-group > summary > a.nav-title");
    for (var t = 0; t < titleLinks.length; t++) {
      titleLinks[t].addEventListener("click", function (ev) {
        ev.stopPropagation();
      });
    }

    // Clicking a child link auto-opens its parent nav-group (in case
    // the user had it collapsed) and, for robustness, also opens the
    // target main-content <details> so jump-to-anchor scrolls land on
    // a visible element.
    var navLinks = document.querySelectorAll("nav.index a[href^='#']");
    for (var j = 0; j < navLinks.length; j++) {
      (function (a) {
        a.addEventListener("click", function () {
          var href = a.getAttribute("href") || "";
          if (!href || href.length < 2) return;
          var id = href.slice(1);
          // Open the parent nav-group in the nav rail.
          var parentNavGroup = a.closest("details.nav-group");
          if (parentNavGroup) parentNavGroup.open = true;
          // Open the main-content target and any ancestor details.
          var target = document.getElementById(id);
          while (target) {
            if (target.tagName === "DETAILS") target.open = true;
            target = target.parentElement && target.parentElement.closest("details");
          }
        });
      })(navLinks[j]);
    }
  }

  function initNavScrollSpy() {
    if (!("IntersectionObserver" in window)) return;
    var navLinks = document.querySelectorAll('nav.index a[href^="#"]');
    if (!navLinks.length) return;
    var byHash = {};
    navLinks.forEach(function (a) { byHash[a.getAttribute("href")] = a; });
    var targets = document.querySelectorAll(MAIN_SECTIONS_SELECTOR);
    var active = null;
    var observer = new IntersectionObserver(
      function (entries) {
        entries.forEach(function (ent) {
          if (!ent.isIntersecting) return;
          var link = byHash["#" + ent.target.id];
          if (!link) return;
          if (active && active !== link) active.classList.remove("active");
          active = link;
          active.classList.add("active");
        });
      },
      { rootMargin: "-30% 0px -60% 0px" }
    );
    for (var i = 0; i < targets.length; i++) observer.observe(targets[i]);
  }

  // --- Advisor severity filter ----------------------------------
  //
  // The counts strip at the top of the Advisor section doubles as a
  // multi-select severity filter. Each chip is an independent toggle:
  // when a severity is off, findings of that class are hidden, and any
  // subsystem group whose children are *all* hidden disappears too.
  // Filter state persists under a ReportID-scoped localStorage key so
  // repeated visits to the same report remember what the reader last
  // focused on.
  function initAdvisorFilter() {
    var summary = document.querySelector(".advisor-summary");
    if (!summary) return;
    var chips = summary.querySelectorAll(".advisor-count");
    if (!chips.length) return;

    // v2 key: new default (Crit + Warn only). The older v1 key used
    // all-severities-on as default, so we bump the namespace rather
    // than migrate silently — otherwise returning readers would see
    // the legacy "everything on" state and never notice the new
    // default-to-signal behaviour.
    var STORAGE_KEY = "mygather:v2:" + REPORT_ID + ":advisor-filter";
    // Default on first load: focus the reader on actionable severities.
    var active = { crit: true, warn: true, info: false, ok: false };
    try {
      var raw = localStorage.getItem(STORAGE_KEY);
      if (raw) {
        var parsed = JSON.parse(raw);
        if (parsed && typeof parsed === "object") {
          ["crit", "warn", "info", "ok"].forEach(function (k) {
            if (typeof parsed[k] === "boolean") active[k] = parsed[k];
          });
        }
      }
    } catch (_) { /* ignore corrupt state */ }

    // Empty-state banner shown when the active filter matches no
    // findings — typically when the default (Crit+Warn) is applied
    // to a capture where the only findings are Info/OK.
    var groupsContainer = document.querySelector(".advisor-groups");
    var emptyBanner = null;
    if (groupsContainer) {
      emptyBanner = document.createElement("p");
      emptyBanner.className = "banner advisor-empty-filter";
      emptyBanner.hidden = true;
      emptyBanner.textContent =
        "No Critical or Warning findings match the current filter — " +
        "toggle Info or OK above to see other findings.";
      groupsContainer.parentNode.insertBefore(emptyBanner, groupsContainer);
    }

    function persist() {
      try { localStorage.setItem(STORAGE_KEY, JSON.stringify(active)); } catch (_) {}
    }

    function apply() {
      // Toggle each chip's active class so the visual state matches.
      chips.forEach(function (chip) {
        var sev = chip.getAttribute("data-sev");
        var on = !!active[sev];
        chip.classList.toggle("is-active", on);
        chip.setAttribute("aria-pressed", on ? "true" : "false");
      });
      // Show/hide findings by severity.
      var findings = document.querySelectorAll(".finding[data-sev]");
      var visibleFindings = 0;
      findings.forEach(function (f) {
        var sev = f.getAttribute("data-sev");
        var hidden = !active[sev];
        f.classList.toggle("is-hidden", hidden);
        if (!hidden) visibleFindings++;
      });
      // Hide subsystem groups with no visible children.
      var groups = document.querySelectorAll(".advisor-group");
      groups.forEach(function (g) {
        var visible = g.querySelectorAll(".finding:not(.is-hidden)").length;
        g.classList.toggle("is-hidden", visible === 0);
      });
      if (emptyBanner) {
        emptyBanner.hidden = visibleFindings !== 0 || findings.length === 0;
      }
    }

    chips.forEach(function (chip) {
      chip.addEventListener("click", function () {
        var sev = chip.getAttribute("data-sev");
        active[sev] = !active[sev];
        apply();
        persist();
      });
    });

    apply();
  }

