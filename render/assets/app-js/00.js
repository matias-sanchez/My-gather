  function initCharts() {
    if (typeof uPlot === "undefined" || !REPORT.charts) return;
    var containers = document.querySelectorAll("[data-chart]");
    for (var i = 0; i < containers.length; i++) {
      var name = containers[i].getAttribute("data-chart");
      var data = REPORT.charts[name];
      if (!data) continue;
      try {
        if (name === "mysqladmin") {
          renderMysqladmin(containers[i], data);
        } else if (name === "iostat") {
          renderIostat(containers[i], data);
        } else if (name === "processlist" && Array.isArray(data.dimensions) && data.dimensions.length > 1) {
          renderProcesslist(containers[i], data);
        } else if (name === "top") {
          renderTopChart(containers[i], data);
        } else if (name === "vmstat") {
          renderVmstatTabs(containers[i], data);
        } else if (name === "innodb-hll") {
          renderHLLSparkline(containers[i], data);
        } else if (name === "network-counters") {
          renderTimeSeries(containers[i], data, {
            label: "events/s",
            title: "Events per second — deltas between kernel counters divided by the time gap between snapshots",
          });
        } else if (name === "network-sockets") {
          renderNetworkSockets(containers[i], data);
        } else {
          renderTimeSeries(containers[i], data, unitForChart(name));
        }
      } catch (e) {
        console && console.warn && console.warn("[my-gather] chart " + name + " failed:", e);
      }
    }
  }

  function unitForChart(name) {
    if (name === "top") {
      return {
        label: "%CPU",
        title: "Per-process CPU utilisation — percent of one logical core",
      };
    }
    if (name === "processlist") {
      return {
        label: "threads",
        title: "Number of connections in each processlist state at each snapshot",
      };
    }
    if (name === "meminfo") {
      return {
        label: "GB",
        title: "Memory value in gigabytes — scaled from /proc/meminfo's kB entries",
      };
    }
    if (name === "vmstat") return "";
    return "";
  }
